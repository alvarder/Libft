#include "libft.h"

void	ft_putnmbr_fd(int nb, int fd)
{
	char	*a;
	int		b;

	b = 0;
	a = ft_itoa(nb);
	while (a[b] != '\0')
	{
		write(fd, &a[b], 1);
		b++;
	}
}
