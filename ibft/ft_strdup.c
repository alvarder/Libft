#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		a;
	char	*s2;

	a = ft_strlen(s1);
	if (!(s2 = malloc((a + 1) * sizeof(char))))
		return (0);
	a = 0;
	while (s1[a] != '\0')
	{
		s2[a] = s1[a];
		a++;
	}
	s2[a] = '\0';
	return (s2);
}
