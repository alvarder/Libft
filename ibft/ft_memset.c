#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char			*y;
	unsigned int	a;

	y = (char *)b;
	a = 0;
	while (a < len)
	{
		y[a] = c;
		a++;
	}
	return (b);
}
